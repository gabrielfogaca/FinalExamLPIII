    private void btnEditClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditClienteActionPerformed
        try {
            Produto produto = new Produto();
            ProdutoDAO produtoDAO = new ProdutoDAO();

            produto.setId_item(idProduto);
            produto.setNome_item(cbxNovoNomeProduto.getText());
            produto.setPreco(Integer.parseInt(cbxNovoPreco.getText()));
            
            produtoDAO.editarProduto(produto);
            JOptionPane.showMessageDialog(null, "Produto editado com sucesso!");

        } catch (SQLException ex) {
            LogController.createLog("Erro ao cadastrar produto", "W");
        }
        LogController.createLog("Produto cadastrado", "I");
    }//GEN-LAST:event_btnEditClienteActionPerformed

