    private void cbxVendaAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_cbxVendaAncestorAdded
        // MOSTRAR OS ID'S DAS VENDAS
        VendaDAO dao = new VendaDAO();
        cbxVenda.removeAll();
        try {
            for (VendaDTO v : dao.listarVendas()) {
                cbxVenda.addItem("Venda ID: " + v.getId_venda());
            }
        } catch (SQLException ex) {
            cbxVenda.addItem("0");
            Logger.getLogger(InserirVenda.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_cbxVendaAncestorAdded
